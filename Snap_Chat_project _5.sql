-- STORY VIEWS
CREATE TABLE story_views (
    view_id VARCHAR(10) PRIMARY KEY,
    story_id VARCHAR(10),
    viewer_id VARCHAR(10),
    view_time DATETIME,
    FOREIGN KEY (story_id) REFERENCES stories(story_id),
    FOREIGN KEY (viewer_id) REFERENCES users(user_id)
);

